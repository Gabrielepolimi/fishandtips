const { createClient } = require('@sanity/client');
const fs = require('fs');
const path = require('path');

// Configurazione Sanity Client
const client = createClient({
  projectId: '3nnnl6gi',
  dataset: 'production',
  apiVersion: '2024-08-10',
  useCdn: false,
});

async function completeIndexingAudit() {
  try {
    console.log('üîç AUDIT COMPLETO PER INDICIZZAZIONE\n');
    console.log('üìÖ Verifica sistematica per ottimizzazione SEO\n');

    // 1. AUDIT ARTICOLI E CONTENUTI
    console.log('üìù 1. AUDIT ARTICOLI E CONTENUTI...');
    
    const articles = await client.fetch(`
      *[_type == "post" && status == "published"] {
        _id,
        title,
        "slug": slug.current,
        publishedAt,
        status,
        mainImage,
        body,
        seoTitle,
        seoDescription,
        seoKeywords,
        categories,
        showYouTubeVideo,
        youtubeUrl,
        youtubeTitle,
        youtubeDescription,
        author,
        excerpt
      } | order(publishedAt desc)
    `);

    console.log(`üìä STATISTICHE ARTICOLI:`);
    console.log(`   üìù Articoli totali: ${articles.length}`);
    console.log(`   üìÖ Articoli pubblicati: ${articles.filter(a => a.status === 'published').length}`);
    console.log(`   üìÖ Articoli con date future: ${articles.filter(a => new Date(a.publishedAt) > new Date()).length}`);
    console.log(`   üìÖ Articoli con date passate: ${articles.filter(a => new Date(a.publishedAt) <= new Date()).length}`);

    // Verifica SEO completo
    const seoComplete = articles.filter(article => 
      article.seoTitle && 
      article.seoDescription && 
      article.seoKeywords &&
      article.seoTitle.length >= 30 &&
      article.seoTitle.length <= 60 &&
      article.seoDescription.length >= 120 &&
      article.seoDescription.length <= 160
    );
    console.log(`   ‚úÖ SEO completo: ${seoComplete.length}/${articles.length} (${Math.round((seoComplete.length/articles.length)*100)}%)`);

    // Verifica immagini
    const withImages = articles.filter(article => article.mainImage);
    console.log(`   üñºÔ∏è Con immagini: ${withImages.length}/${articles.length} (${Math.round((withImages.length/articles.length)*100)}%)`);

    // Verifica contenuto
    const withContent = articles.filter(article => article.body && article.body.length > 0);
    console.log(`   üìÑ Con contenuto: ${withContent.length}/${articles.length} (${Math.round((withContent.length/articles.length)*100)}%)`);

    // Verifica video YouTube
    const withVideo = articles.filter(article => article.showYouTubeVideo);
    console.log(`   üé• Con video YouTube: ${withVideo.length}/${articles.length} (${Math.round((withVideo.length/articles.length)*100)}%)`);

    // 2. AUDIT CATEGORIE
    console.log('\nüìÇ 2. AUDIT CATEGORIE...');
    
    const categories = await client.fetch(`
      *[_type == "category"] {
        _id,
        title,
        "slug": slug.current,
        description
      }
    `);
    
    console.log(`üìä STATISTICHE CATEGORIE:`);
    console.log(`   üìÇ Categorie totali: ${categories.length}`);
    
    const categoriesWithSlug = categories.filter(cat => cat.slug);
    console.log(`   ‚úÖ Con slug: ${categoriesWithSlug.length}/${categories.length}`);
    
    const categoriesWithoutSlug = categories.filter(cat => !cat.slug);
    console.log(`   ‚ùå Senza slug: ${categoriesWithoutSlug.length}/${categories.length}`);
    
    if (categoriesWithoutSlug.length > 0) {
      console.log(`   üìã Categorie senza slug:`);
      categoriesWithoutSlug.forEach(cat => {
        console.log(`      - ${cat.title} (ID: ${cat._id})`);
      });
    }

    // Verifica articoli per categoria
    const categoryStats = {};
    articles.forEach(article => {
      if (article.categories) {
        article.categories.forEach(cat => {
          const categoryName = typeof cat === 'object' ? cat.title : cat;
          categoryStats[categoryName] = (categoryStats[categoryName] || 0) + 1;
        });
      }
    });
    
    console.log(`   üìä Distribuzione articoli per categoria:`);
    Object.entries(categoryStats).forEach(([category, count]) => {
      console.log(`      - ${category}: ${count} articoli`);
    });

    // 3. AUDIT SITEMAP
    console.log('\nüó∫Ô∏è 3. AUDIT SITEMAP...');
    
    const sitemapPath = path.join(process.cwd(), 'public/sitemap-static.xml');
    if (fs.existsSync(sitemapPath)) {
      const sitemapContent = fs.readFileSync(sitemapPath, 'utf8');
      const urlMatches = sitemapContent.match(/<loc>(.*?)<\/loc>/g);
      const sitemapUrls = urlMatches ? urlMatches.map(match => match.replace(/<\/?loc>/g, '')) : [];
      
      console.log(`üìä STATISTICHE SITEMAP:`);
      console.log(`   üìÑ Sitemap trovata: ‚úÖ`);
      console.log(`   üîó URL totali: ${sitemapUrls.length}`);
      
      // Verifica articoli in sitemap
      const articlesInSitemap = articles.filter(article => 
        sitemapUrls.includes(`https://fishandtips.it/articoli/${article.slug}`)
      );
      console.log(`   üìù Articoli in sitemap: ${articlesInSitemap.length}/${articles.length}`);
      
      // Verifica categorie in sitemap
      const categoriesInSitemap = categoriesWithSlug.filter(cat => 
        sitemapUrls.includes(`https://fishandtips.it/categoria/${cat.slug}`)
      );
      console.log(`   üìÇ Categorie in sitemap: ${categoriesInSitemap.length}/${categoriesWithSlug.length}`);
      
      // Verifica URL mancanti
      const missingArticles = articles.filter(article => 
        !sitemapUrls.includes(`https://fishandtips.it/articoli/${article.slug}`)
      );
      if (missingArticles.length > 0) {
        console.log(`   ‚ùå Articoli mancanti dalla sitemap: ${missingArticles.length}`);
        missingArticles.forEach(article => {
          console.log(`      - ${article.title} (${article.slug})`);
        });
      }
      
      // Verifica URL duplicati
      const urlCounts = {};
      sitemapUrls.forEach(url => {
        urlCounts[url] = (urlCounts[url] || 0) + 1;
      });
      const duplicateUrls = Object.entries(urlCounts).filter(([url, count]) => count > 1);
      console.log(`   üîÑ URL duplicati: ${duplicateUrls.length}`);
      
    } else {
      console.log(`   ‚ùå Sitemap non trovata!`);
    }

    // 4. AUDIT ROBOTS.TXT
    console.log('\nü§ñ 4. AUDIT ROBOTS.TXT...');
    
    const robotsPath = path.join(process.cwd(), 'app/robots.ts');
    if (fs.existsSync(robotsPath)) {
      const robotsContent = fs.readFileSync(robotsPath, 'utf8');
      console.log('‚úÖ robots.ts trovato');
      
      // Verifica regole critiche
      const criticalRules = [
        'sitemap:',
        'Disallow: /api/',
        'Disallow: /_next/',
        'Disallow: /favicon.ico',
        'Disallow: /manifest.webmanifest',
        'Disallow: /feed.xml',
        'Disallow: /sitemap.xml',
        'Disallow: /articoli?search=*'
      ];
      
      let rulesPresent = 0;
      criticalRules.forEach(rule => {
        if (robotsContent.includes(rule)) {
          console.log(`   ‚úÖ ${rule}`);
          rulesPresent++;
        } else {
          console.log(`   ‚ùå ${rule}`);
        }
      });
      
      console.log(`   üìä Regole presenti: ${rulesPresent}/${criticalRules.length} (${Math.round((rulesPresent/criticalRules.length)*100)}%)`);
      
    } else {
      console.log('‚ùå robots.ts non trovato!');
    }

    // 5. AUDIT METADATA E SEO
    console.log('\nüìä 5. AUDIT METADATA E SEO...');
    
    // Analisi SEO dettagliata
    const seoIssues = [];
    
    articles.forEach(article => {
      const issues = [];
      
      if (!article.seoTitle) {
        issues.push('SEO Title mancante');
      } else if (article.seoTitle.length < 30) {
        issues.push(`SEO Title troppo corto (${article.seoTitle.length} chars)`);
      } else if (article.seoTitle.length > 60) {
        issues.push(`SEO Title troppo lungo (${article.seoTitle.length} chars)`);
      }
      
      if (!article.seoDescription) {
        issues.push('SEO Description mancante');
      } else if (article.seoDescription.length < 120) {
        issues.push(`SEO Description troppo corta (${article.seoDescription.length} chars)`);
      } else if (article.seoDescription.length > 160) {
        issues.push(`SEO Description troppo lunga (${article.seoDescription.length} chars)`);
      }
      
      if (!article.seoKeywords) {
        issues.push('SEO Keywords mancanti');
      }
      
      if (issues.length > 0) {
        seoIssues.push({
          title: article.title,
          slug: article.slug,
          issues: issues
        });
      }
    });
    
    console.log(`üìä STATISTICHE SEO:`);
    console.log(`   ‚úÖ Articoli con SEO ottimizzato: ${articles.length - seoIssues.length}/${articles.length}`);
    console.log(`   ‚ùå Articoli con problemi SEO: ${seoIssues.length}/${articles.length}`);
    
    if (seoIssues.length > 0) {
      console.log(`   üìã Articoli con problemi SEO:`);
      seoIssues.forEach(article => {
        console.log(`      - ${article.title}`);
        article.issues.forEach(issue => {
          console.log(`        ‚ùå ${issue}`);
        });
      });
    }

    // 6. AUDIT STRUTTURA URL
    console.log('\nüîó 6. AUDIT STRUTTURA URL...');
    
    // Verifica slug duplicati
    const slugCounts = {};
    articles.forEach(article => {
      slugCounts[article.slug] = (slugCounts[article.slug] || 0) + 1;
    });
    
    const duplicateSlugs = Object.entries(slugCounts).filter(([slug, count]) => count > 1);
    console.log(`üìä STATISTICHE URL:`);
    console.log(`   üîÑ Slug duplicati: ${duplicateSlugs.length}`);
    
    // Verifica slug problematici
    const problematicSlugs = articles.filter(article => 
      article.slug.includes('--') || 
      article.slug.endsWith('-') || 
      article.slug.startsWith('-') ||
      article.slug.length > 100 ||
      article.slug.includes(' ') ||
      article.slug.includes('_')
    );
    console.log(`   ‚ùå Slug problematici: ${problematicSlugs.length}`);
    
    if (problematicSlugs.length > 0) {
      console.log(`   üìã Slug problematici:`);
      problematicSlugs.forEach(article => {
        console.log(`      - ${article.slug} (${article.title})`);
      });
    }

    // 7. AUDIT IMMAGINI E MEDIA
    console.log('\nüñºÔ∏è 7. AUDIT IMMAGINI E MEDIA...');
    
    const articlesWithImages = articles.filter(article => article.mainImage);
    const articlesWithoutImages = articles.filter(article => !article.mainImage);
    
    console.log(`üìä STATISTICHE IMMAGINI:`);
    console.log(`   ‚úÖ Articoli con immagini: ${articlesWithImages.length}/${articles.length}`);
    console.log(`   ‚ùå Articoli senza immagini: ${articlesWithoutImages.length}/${articles.length}`);
    
    if (articlesWithoutImages.length > 0) {
      console.log(`   üìã Articoli senza immagini:`);
      articlesWithoutImages.forEach(article => {
        console.log(`      - ${article.title}`);
      });
    }
    
    // Verifica video YouTube
    const articlesWithVideo = articles.filter(article => article.showYouTubeVideo);
    const completeVideos = articlesWithVideo.filter(article => 
      article.youtubeUrl && article.youtubeTitle && article.youtubeDescription
    );
    
    console.log(`   üé• Articoli con video YouTube: ${articlesWithVideo.length}`);
    console.log(`   ‚úÖ Video completi: ${completeVideos.length}/${articlesWithVideo.length}`);
    
    if (articlesWithVideo.length > 0 && completeVideos.length < articlesWithVideo.length) {
      console.log(`   üìã Video incompleti:`);
      articlesWithVideo.forEach(article => {
        if (!article.youtubeUrl || !article.youtubeTitle || !article.youtubeDescription) {
          console.log(`      - ${article.title}`);
          if (!article.youtubeUrl) console.log(`        ‚ùå URL mancante`);
          if (!article.youtubeTitle) console.log(`        ‚ùå Titolo mancante`);
          if (!article.youtubeDescription) console.log(`        ‚ùå Descrizione mancante`);
        }
      });
    }

    // 8. AUDIT PERFORMANCE E TECNICA
    console.log('\n‚ö° 8. AUDIT PERFORMANCE E TECNICA...');
    
    const now = new Date();
    const futureArticles = articles.filter(article => new Date(article.publishedAt) > now);
    const recentArticles = articles.filter(article => 
      new Date(article.publishedAt) > new Date(now.getTime() - 30 * 24 * 60 * 60 * 1000)
    );
    const oldArticles = articles.filter(article => 
      new Date(article.publishedAt) < new Date(now.getTime() - 60 * 24 * 60 * 60 * 1000)
    );
    
    console.log(`üìä STATISTICHE TEMPORALI:`);
    console.log(`   üìÖ Articoli con date future: ${futureArticles.length}`);
    console.log(`   üìÖ Articoli pubblicati negli ultimi 30 giorni: ${recentArticles.length}`);
    console.log(`   üìÖ Articoli pubblicati pi√π di 60 giorni fa: ${oldArticles.length}`);
    
    // Verifica autori
    const authors = [...new Set(articles.map(article => article.author))];
    console.log(`   üë• Autori unici: ${authors.length}`);
    
    // Verifica excerpt
    const articlesWithExcerpt = articles.filter(article => article.excerpt && article.excerpt.length > 0);
    console.log(`   üìù Articoli con excerpt: ${articlesWithExcerpt.length}/${articles.length}`);

    // 9. ANALISI PROBLEMI CRITICI
    console.log('\nüö® 9. ANALISI PROBLEMI CRITICI...');
    
    const criticalIssues = [];
    
    if (categoriesWithoutSlug.length > 0) {
      criticalIssues.push(`üî¥ CRITICO: ${categoriesWithoutSlug.length} categorie senza slug`);
    }
    
    if (seoIssues.length > 0) {
      criticalIssues.push(`üü° ALTO: ${seoIssues.length} articoli con problemi SEO`);
    }
    
    if (articlesWithoutImages.length > 0) {
      criticalIssues.push(`üü° ALTO: ${articlesWithoutImages.length} articoli senza immagini`);
    }
    
    if (duplicateSlugs.length > 0) {
      criticalIssues.push(`üî¥ CRITICO: ${duplicateSlugs.length} slug duplicati`);
    }
    
    if (problematicSlugs.length > 0) {
      criticalIssues.push(`üü° ALTO: ${problematicSlugs.length} slug problematici`);
    }
    
    if (criticalIssues.length > 0) {
      console.log(`üö® PROBLEMI CRITICI IDENTIFICATI:`);
      criticalIssues.forEach(issue => console.log(`   ${issue}`));
    } else {
      console.log(`‚úÖ Nessun problema critico identificato`);
    }

    // 10. CALCOLO HEALTH SCORE
    console.log('\nüèÜ 10. CALCOLO HEALTH SCORE...');
    
    let score = 0;
    let maxScore = 0;
    
    // SEO Score (25%)
    maxScore += 25;
    score += (seoComplete.length / articles.length) * 25;
    
    // Images Score (20%)
    maxScore += 20;
    score += (withImages.length / articles.length) * 20;
    
    // Content Score (20%)
    maxScore += 20;
    score += (withContent.length / articles.length) * 20;
    
    // Categories Score (15%)
    maxScore += 15;
    score += (categoriesWithSlug.length / categories.length) * 15;
    
    // URL Structure Score (10%)
    maxScore += 10;
    score += (articles.length - problematicSlugs.length - duplicateSlugs.length) / articles.length * 10;
    
    // Video Score (10%)
    maxScore += 10;
    score += (completeVideos.length / Math.max(articlesWithVideo.length, 1)) * 10;
    
    const healthScore = Math.round(score);
    
    console.log(`üìä HEALTH SCORE: ${healthScore}%`);
    
    if (healthScore >= 90) {
      console.log(`‚úÖ ECCELLENTE: Sistema SEO ottimizzato`);
    } else if (healthScore >= 80) {
      console.log(`üü¢ BUONO: Alcuni miglioramenti necessari`);
    } else if (healthScore >= 70) {
      console.log(`üü° MEDIO: Interventi significativi necessari`);
    } else if (healthScore >= 60) {
      console.log(`üü† BASSO: Interventi urgenti necessari`);
    } else {
      console.log(`üî¥ CRITICO: Interventi immediati necessari`);
    }

    // 11. RACCOMANDAZIONI PRIORITARIE
    console.log('\nüí° 11. RACCOMANDAZIONI PRIORITARIE...');
    
    const recommendations = [];
    
    if (categoriesWithoutSlug.length > 0) {
      recommendations.push({
        priority: 'üî¥ CRITICO',
        action: 'Fix categorie senza slug',
        description: 'Aggiornare slug delle categorie in Sanity Studio',
        impact: 'Pagine categoria non funzionano'
      });
    }
    
    if (duplicateSlugs.length > 0) {
      recommendations.push({
        priority: 'üî¥ CRITICO',
        action: 'Fix slug duplicati',
        description: 'Correggere slug duplicati immediatamente',
        impact: 'Conflitti di URL'
      });
    }
    
    if (seoIssues.length > 0) {
      recommendations.push({
        priority: 'üü° ALTO',
        action: 'Fix SEO articoli',
        description: 'Aggiornare SEO title, description e keywords',
        impact: 'Indicizzazione compromessa'
      });
    }
    
    if (articlesWithoutImages.length > 0) {
      recommendations.push({
        priority: 'üü° ALTO',
        action: 'Fix immagini articoli',
        description: 'Aggiungere immagini principali agli articoli',
        impact: 'SEO e user experience compromessi'
      });
    }
    
    if (recommendations.length > 0) {
      console.log(`üìã RACCOMANDAZIONI PRIORITARIE:`);
      recommendations.forEach((rec, index) => {
        console.log(`   ${index + 1}. ${rec.priority} ${rec.action}`);
        console.log(`      üìù ${rec.description}`);
        console.log(`      üí• ${rec.impact}`);
      });
    } else {
      console.log(`‚úÖ Nessuna raccomandazione critica`);
    }

    // 12. RIEPILOGO FINALE
    console.log('\nüìä 12. RIEPILOGO FINALE...');
    console.log(`üìù Articoli totali: ${articles.length}`);
    console.log(`üìÇ Categorie totali: ${categories.length}`);
    console.log(`‚úÖ SEO ottimizzato: ${seoComplete.length}`);
    console.log(`‚ùå SEO problematico: ${seoIssues.length}`);
    console.log(`‚úÖ Con immagini: ${withImages.length}`);
    console.log(`‚ùå Senza immagini: ${articlesWithoutImages.length}`);
    console.log(`‚úÖ Con contenuto: ${withContent.length}`);
    console.log(`üé• Con video YouTube: ${articlesWithVideo.length}`);
    console.log(`‚úÖ Video completi: ${completeVideos.length}`);
    console.log(`‚úÖ Categorie con slug: ${categoriesWithSlug.length}`);
    console.log(`‚ùå Categorie senza slug: ${categoriesWithoutSlug.length}`);
    console.log(`üîÑ Slug duplicati: ${duplicateSlugs.length}`);
    console.log(`‚ùå Slug problematici: ${problematicSlugs.length}`);
    
    console.log(`\nüéØ RISULTATO FINALE:`);
    console.log(`üèÜ Health Score: ${healthScore}%`);
    console.log(`üö® Problemi critici: ${criticalIssues.length}`);
    console.log(`üí° Raccomandazioni: ${recommendations.length}`);

  } catch (error) {
    console.error('‚ùå Errore durante l\'audit completo:', error);
  }
}

completeIndexingAudit();
