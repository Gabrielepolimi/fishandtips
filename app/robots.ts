import { MetadataRoute } from 'next';

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: '*',
        allow: '/',
        disallow: [
          '/api/',
          '/admin/',
          '/dashboard/',
          '/_next/',
          '/newsletter/disiscrizione',
          '/*?search=',
          '/*&search=',
        ],
      },
    ],
    sitemap: 'https://fishandtips.it/sitemap.xml',
    host: 'https://fishandtips.it',
  };
}
